class Solution
{
    public static Node reverse(Node node, int k)
    {
        ArrayList<Integer> a1=new ArrayList<>();
         ArrayList<Integer> a2=new ArrayList<>();
        int t=k;
        Node n1=node;
        while(n1!=null)
        {
            if(t==0)
            {
                Collections.reverse(a1);
                a2.addAll(a1);
                a1.clear();
                t=k;
            }
            a1.add(n1.data);
            n1=n1.next;
            t--;
        }
         Collections.reverse(a1);
         a2.addAll(a1);
         n1=node;
         for(int i=0;i<a2.size();i++)
         {
             n1.data=a2.get(i);
             n1=n1.next;
         }
        return node;
    }
}

